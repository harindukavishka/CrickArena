    private void tableScheduleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableScheduleMouseClicked
        int index = tableSchedule.getSelectedRow();
        TableModel model = tableSchedule.getModel();
        String id = model.getValueAt(index, 0).toString();
        matchPk = Integer.parseInt(id);
        
        String name = model.getValueAt(index, 1).toString();
        txtSTeams.setText(name);
        String date = model.getValueAt(index, 2).toString();
        txtSTeams.setText(date);
        String venue = model.getValueAt(index, 3).toString();
        txtSTeams.setText(venue);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableScheduleMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String teams = txtSTeams.getText();
        String date = txtSDate.getText();
        String venue = txtSVenue.getText();
        
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update schedule set teams=?,date=?,venue=? where match_pk=?");
                ps.setString(1, teams);
                ps.setString(2, date);
                ps.setString(3, venue);
                ps.setInt(4, matchPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Match Updated Successfully");
                setVisible(false);
                new ManageTeams().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

