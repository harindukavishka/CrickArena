    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model =(DefaultTableModel) tablePoint.getModel();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from point");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("point_pk"),rs.getString("position"),rs.getString("team"),rs.getString("lost"),rs.getString("won"),rs.getString("nrr"),rs.getString("point")});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String position = txtPosition.getText();
        String team = txtTeam.getText();
        String lost = txtLost.getText();
        String won = txtWon.getText();
        String nrr = txtNrr.getText();
        String point = txtPoint.getText();
        
        if(validateFields("edit")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into point(position,team,lost,won,nrr,point) values (?,?,?,?,?,?)");
                ps.setString(1, position);
                ps.setString(2, team);
                ps.setString(3, lost);
                ps.setString(4, won);
                ps.setString(5, nrr);
                ps.setString(6, point);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Team Added Successfully");
                setVisible(false);
                new ManagePointTable().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

