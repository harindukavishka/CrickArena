    private void tablePointMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePointMouseClicked
        int index = tablePoint.getSelectedRow();
        TableModel model = tablePoint.getModel();
        String id = model.getValueAt(index, 0).toString();
        pointPk = Integer.parseInt(id);
        
        String pos = model.getValueAt(index, 1).toString();
        txtPosition.setText(pos);
        String team = model.getValueAt(index, 2).toString();
        txtTeam.setText(team);
        String lost = model.getValueAt(index, 3).toString();
        txtLost.setText(lost);
        String won = model.getValueAt(index, 4).toString();
        txtWon.setText(won);
        String nrr = model.getValueAt(index, 5).toString();
        txtNrr.setText(nrr);
        String point = model.getValueAt(index, 6).toString();
        txtPoint.setText(point);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tablePointMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String position = txtPosition.getText();
        String team = txtTeam.getText();
        String lost = txtLost.getText();
        String won = txtWon.getText();
        String nrr = txtNrr.getText();
        String point = txtPoint.getText();
        
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update point set position=?,team=?,lost=?,won=?,nrr=?,point=? where point_pk=?");
                ps.setString(1, position);
                ps.setString(2, team);
                ps.setString(3, lost);
                ps.setString(4, won);
                ps.setString(5, nrr);
                ps.setString(6, point);
                ps.setInt(7, pointPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "point table Updated Successfully");
                setVisible(false);
                new ManageTeams().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

