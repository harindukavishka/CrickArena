    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String teams = txtRTeams.getText();
        String score = txtRScores.getText();
        String result = txtResult.getText();
        
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update result set teams=?,score=?,result=? where result_pk=?");
                ps.setString(1, teams);
                ps.setString(2, score);
                ps.setString(3, result);
                ps.setInt(4, resultPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Team Updated Successfully");
                setVisible(false);
                new ManageResult().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tableResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableResultMouseClicked
        int index = tableResult.getSelectedRow();
        TableModel model = tableResult.getModel();
        String id = model.getValueAt(index, 0).toString();
        resultPk = Integer.parseInt(id);
        
        String name = model.getValueAt(index, 1).toString();
        txtRTeams.setText(name);
        String score = model.getValueAt(index, 2).toString();
        txtRScores.setText(score);
        String result = model.getValueAt(index, 3).toString();
        txtResult.setText(result);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableResultMouseClicked

