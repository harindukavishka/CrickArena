    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model =(DefaultTableModel) tableRecodes.getModel();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from recodes");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("recode_pk"),rs.getString("recode"),rs.getString("player")});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String recode = txtRecode.getText();
        String player = txtPlayer.getText();
        
        if(validateFields("edit")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into recodes(recode,player) values (?,?)");
                ps.setString(1, recode);
                ps.setString(2, player);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Recode Added Successfully");
                setVisible(false);
                new ManagePointTable().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

