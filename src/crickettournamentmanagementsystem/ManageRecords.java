    private void tableRecodesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRecodesMouseClicked
        int index = tableRecodes.getSelectedRow();
        TableModel model = tableRecodes.getModel();
        String id = model.getValueAt(index, 0).toString();
        recodePk = Integer.parseInt(id);
        
        String recode = model.getValueAt(index, 1).toString();
        txtRecode.setText(recode);
        String player = model.getValueAt(index, 2).toString();
        txtPlayer.setText(player);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableRecodesMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String recode = txtRecode.getText();
        String player = txtPlayer.getText();
        
        if(validateFields("edit")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update recodes set recode=?,player=? where recode_pk=?");
                ps.setString(1, recode);
                ps.setString(2, player);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Recode table Updated Successfully");
                setVisible(false);
                new ManageRecords().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

