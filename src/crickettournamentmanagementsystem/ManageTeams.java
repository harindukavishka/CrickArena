    private void tableTeamsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTeamsMouseClicked
        int index = tableTeams.getSelectedRow();
        TableModel model = tableTeams.getModel();
        String id = model.getValueAt(index, 0).toString();
        teamPk = Integer.parseInt(id);
        
        String name = model.getValueAt(index, 1).toString();
        txtTName.setText(name);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableTeamsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String teamName = txtTName.getText();
        String teamOwner = txtTOwner.getText();
        String teamCode = txtTCode.getText();
        
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update teams set teameName=?,teamOwner=?,teamCode=? where team_pk=?");
                ps.setString(1, teamName);
                ps.setString(2, teamOwner);
                ps.setString(3, teamCode);
                ps.setInt(4, teamPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Team Updated Successfully");
                setVisible(false);
                new ManageTeams().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

