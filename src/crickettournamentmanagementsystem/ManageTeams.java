    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String teamName = txtTName.getText();
        String teamOwner = txtTOwner.getText();
        String teamCode = txtTCode.getText();
        
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null, "All Fields are requred");
        }else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into teams(teameName,teamOwner,teamCode) values (?,?,?)");
                ps.setString(1, teamName);
                ps.setString(2, teamOwner);
                ps.setString(3, teamCode);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Team Added Successfully");
                setVisible(false);
                new ManageTeams().setVisible(true);
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

