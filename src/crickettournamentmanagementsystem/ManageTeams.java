    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
       DefaultTableModel model =(DefaultTableModel) tableTeams.getModel();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from teams");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("team_pk"),rs.getString("teameName"),rs.getString("teamOwner"),rs.getString("teamCode")});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

